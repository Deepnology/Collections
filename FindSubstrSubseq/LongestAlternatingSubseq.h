/*
Leetcode: Wiggle Subsequence (the first diff may be incr or decr)
https://leetcode.com/articles/wiggle-subsequence/
Elements of programming interview, DynamicProgramming: Find the longest nondecreasing subsequence, variant
http://stackoverflow.com/questions/6914969/dynamic-programming-find-longest-subsequence-that-is-zig-zag
variation of LIS
Define a sequence of numbers[a0, a1, a2, ..., a(n-1)] to be alternating if ai < a(i+1) for even i and ai > a(i+1) for odd i.
Given an array of numbers A of length n, find a longest subsequence [i0, i1, i2, ..., i(k-1)] such that [A[i0], A[i1], A[i2], ..., A[i(k-1)]] is alternating.

DP1D: O(n^2) time, O(n) space
LinearDP1D: O(n) time, O(n) or O(1) space
Greedy: O(n) time, O(1) space
*/
class LongestAlternatingSubseq
{
public:
	LongestAlternatingSubseq(){}
	~LongestAlternatingSubseq(){}

	int DP1D_DecrFirst(const std::vector<int> & v)
	{
		int N = v.size();
		std::vector<int> dp(N, 1);//longest length at each index
		std::vector<int> trend(N);//0: decr, 1: incr
		trend[0] = 1;//next trend should be 0 (decr)
		/*
		trend[i]: means the trend between v[i-1] and v[i], which also means the next trend between v[i] and v[i+1] should be !trend[i]
		*/
		int maxAltCount = 1;
		int maxAltIdx;//the last index of the LAS
		std::vector<int> prevIdx(N, -1);//previous index of LAS at each index
		for (int curEnd = 1; curEnd < N; ++curEnd)
		{
			if (dp[curEnd - 1] == 1)
				trend[curEnd - 1] = 1;//next trend should be 0 (decr)

			for (int curStart = 0; curStart < curEnd; ++curStart)
			{
				if (((!trend[curStart] && v[curStart] < v[curEnd]) || (trend[curStart] && v[curStart] > v[curEnd])) && dp[curStart] + 1 > dp[curEnd])
				{
					dp[curEnd] = dp[curStart] + 1;
					trend[curEnd] = !trend[curStart];
					prevIdx[curEnd] = curStart;
				}
			}

			if (dp[curEnd] > maxAltCount)
			{
				maxAltCount = dp[curEnd];
				maxAltIdx = curEnd;
			}

			std::cout << "dp:    " << Debug::ToStr1D<int>()(dp) << std::endl;
			std::cout << "trend: " << Debug::ToStr1D<int>()(trend) << std::endl;
		}

		std::vector<int> las(maxAltCount);
		int lasLen = maxAltCount;
		while (lasLen-- > 0)
		{
			las[lasLen] = v[maxAltIdx];
			maxAltIdx = prevIdx[maxAltIdx];
		}
		std::cout << "LongestAlternatingSubseq DP1D_DecrFirst for \"" << Debug::ToStr1D<int>()(v) << "\": " << maxAltCount << " (" << Debug::ToStr1D<int>()(las) << ")" << std::endl;
		return maxAltCount;
	}


	int DP1D(const std::vector<int> & v)//better
	{
		int N = v.size();
		std::vector<int> dpDecr(N, 1);//longest length with a DECR ending at each index
		std::vector<int> dpIncr(N, 1);//longest length with an INCR ending at each index
		int maxAltCount = 1;
		int maxAltIdx;//the last index of the LAS
		std::vector<int> prevIdx(N, -1);//previous index of LAS at each index
		for (int curEnd = 1; curEnd < N; ++curEnd)
		{
			for (int curStart = 0; curStart < curEnd; ++curStart)
			{
				if (v[curStart] > v[curEnd] && dpIncr[curStart] + 1 > dpDecr[curEnd])
				{
					dpDecr[curEnd] = dpIncr[curStart] + 1;
					prevIdx[curEnd] = curStart;
				}
				else if (v[curStart] < v[curEnd] && dpDecr[curStart] + 1 > dpIncr[curEnd])
				{
					dpIncr[curEnd] = dpDecr[curStart] + 1;
					prevIdx[curEnd] = curStart;
				}
			}

			if (std::max(dpDecr[curEnd], dpIncr[curEnd]) > maxAltCount)
			{
				maxAltCount = std::max(dpDecr[curEnd], dpIncr[curEnd]);
				maxAltIdx = curEnd;
			}

			std::cout << "dpDecr:  " << Debug::ToStr1D<int>()(dpDecr) << std::endl;
			std::cout << "dpIncr: " << Debug::ToStr1D<int>()(dpIncr) << std::endl;
		}

		std::vector<int> las(maxAltCount);
		int lasLen = maxAltCount;
		while (lasLen-- > 0)
		{
			las[lasLen] = v[maxAltIdx];
			maxAltIdx = prevIdx[maxAltIdx];
		}
		std::cout << "LongestAlternatingSubseq DP1D for \"" << Debug::ToStr1D<int>()(v) << "\": " << maxAltCount << " (" << Debug::ToStr1D<int>()(las) << ")" << std::endl;
		return maxAltCount;
	}

	int LinearDP1D(const std::vector<int> & v)//best, this approach can be reduced to O(1) space
	{
		int N = v.size();
		if (N < 2) return N;

		std::vector<int> dpDecr(N, 1);
		std::vector<int> dpIncr(N, 1);
		for (int i = 1; i < N; ++i)
		{
			if (v[i] > v[i - 1])//2 cases: can be either a turn or an extension of incr
			{
				//no matter in which case, these 2 rules won't change
				dpIncr[i] = dpDecr[i - 1] + 1;
				dpDecr[i] = dpDecr[i - 1];
			}
			else if (v[i] < v[i - 1])//2 cases: can be either a turn of an extension of decr
			{
				//no matter in which case, these 2 rules won't change
				dpDecr[i] = dpIncr[i - 1] + 1;
				dpIncr[i] = dpIncr[i - 1];
			}
			else
			{
				dpIncr[i] = dpIncr[i - 1];
				dpDecr[i] = dpDecr[i - 1];
			}
		}

		std::cout << "LongestAlternatingSubseq LinearDP1D for \"" << Debug::ToStr1D<int>()(v) << "\": " << std::max(dpIncr[N - 1], dpDecr[N - 1]) << std::endl;
		return std::max(dpIncr[N - 1], dpDecr[N - 1]);
	}

	int Greedy(const std::vector<int> & v)
	{
		int N = v.size();
		if (N < 2) return N;

		int prevDiff = v[1] - v[0];
		int count = prevDiff != 0 ? 2 : 1;
		for (int i = 2; i < N; ++i)
		{
			int diff = v[i] - v[i - 1];
			if ((diff > 0 && prevDiff <= 0) || (diff < 0 && prevDiff >= 0))
			{
				++count;
				prevDiff = diff;
			}
		}

		std::cout << "LongestAlternatingSubseq Greedy for \"" << Debug::ToStr1D<int>()(v) << "\": " << count << std::endl;
		return count;
	}
};
